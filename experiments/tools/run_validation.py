"""One-shot validation orchestrator for a completed Slurm job.

Given a scenario directory (containing hops.base.yaml) and a Slurm job id
(whose output lives at <scenario>/output/<job-id>/), this tool materializes
three HOPS variants, runs each, and emits comparison.json + report.md under
<job-dir>/derived/.

Variants:
  - no_lookahead:    base HOPS config only (copied from <job-dir>/hops_summary.json).
  - link_calibrated: base + measured link_bench overrides.
  - trace_replay:    base + link overrides + per-stage forward distribution fit
                     from <job-dir>/megatron_trace/.

If link_bench results are not found, link_calibrated and trace_replay are
skipped and the manifest records that.

Usage:
    uv run python experiments/tools/run_validation.py \\
        --scenario experiments/experiment_1/1_all_nodes \\
        --job-id 12 \\
        [--link-bench-dir <path>]
"""

from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
from pathlib import Path

_TOOLS_DIR = Path(__file__).resolve().parent
if str(_TOOLS_DIR) not in sys.path:
    sys.path.insert(0, str(_TOOLS_DIR))

import yaml

import compare_run  # noqa: E402
from derive_megatron_stats import (  # noqa: E402
    build_overlay as build_stage_overlay,
    fit_stage_distributions,
)
from materialize_hops_variant import materialize  # noqa: E402
from parse_link_bench import (  # noqa: E402
    _iter_jsonl_rows,
    _stage_to_device,
    build_overrides_yaml,
    derive_pair_measurements,
)


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _import_hops_trace_modules():
    sys.path.insert(0, str(_repo_root() / "src"))
    from hops.core.types import Phase  # type: ignore[import-not-found]
    from hops.megatron.importer import load_raw_megatron_events  # type: ignore[import-not-found]
    return load_raw_megatron_events, Phase


def _write_yaml_with_banner(path: Path, banner: str, document: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        handle.write(banner)
        yaml.safe_dump(document, handle, sort_keys=False)


def _run_hops(base_config_path: Path, summary_path: Path) -> None:
    """Invoke main.py to run HOPS on a config and emit summary JSON."""
    cmd = [
        "uv", "run", "python", "main.py",
        "--config", str(base_config_path),
        "--no-viz",
        "--summary-json", str(summary_path),
    ]
    summary_path.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(cmd, check=True, cwd=_repo_root())


def _write_no_lookahead_summary(job_dir: Path, derived_dir: Path) -> bool:
    existing = job_dir / "hops_summary.json"
    destination = derived_dir / "hops_no_lookahead_summary.json"
    if existing.exists():
        shutil.copyfile(existing, destination)
        return True
    # Fall back to re-running HOPS on the committed base if the inline summary
    # was never captured (e.g. legacy run artifact).
    base = job_dir.parent.parent / "hops.base.yaml"
    if not base.exists():
        return False
    _run_hops(base, destination)
    return True


def _generate_links_overlay(
    link_bench_dir: Path,
    trace_map_path: Path,
    output_path: Path,
) -> bool:
    jsonl_paths = sorted(link_bench_dir.glob("*.jsonl"))
    if not jsonl_paths:
        return False
    trace_map = json.loads(trace_map_path.read_text(encoding="utf-8"))
    stage_to_device = _stage_to_device(trace_map)
    rows = _iter_jsonl_rows(jsonl_paths)
    measurements = derive_pair_measurements(rows)
    document = build_overrides_yaml(measurements, stage_to_device)
    banner = (
        "# GENERATED by experiments/tools/run_validation.py (parse_link_bench). DO NOT EDIT.\n"
        f"# Source: {link_bench_dir}\n"
    )
    _write_yaml_with_banner(output_path, banner, document)
    return True


def _generate_stage_timings_overlay(
    job_dir: Path,
    output_path: Path,
    min_iteration: int,
) -> bool:
    trace_dir = job_dir / "megatron_trace"
    if not trace_dir.is_dir():
        return False
    try:
        next(trace_dir.glob("*.jsonl"))
    except StopIteration:
        return False
    load_raw_megatron_events, Phase = _import_hops_trace_modules()
    events = load_raw_megatron_events(trace_dir)
    fits = fit_stage_distributions(events, forward_phase=Phase.FORWARD, min_iteration=min_iteration)
    if not fits:
        return False
    document = build_stage_overlay(fits)
    banner = (
        "# GENERATED by experiments/tools/run_validation.py (derive_megatron_stats). DO NOT EDIT.\n"
        f"# Source: {trace_dir} (FORWARD compute events only).\n"
    )
    _write_yaml_with_banner(output_path, banner, document)
    return True


def run(
    scenario_dir: Path,
    job_id: str,
    link_bench_dir: Path | None = None,
    min_iteration: int = 0,
) -> Path:
    base_config = scenario_dir / "hops.base.yaml"
    if not base_config.exists():
        raise SystemExit(f"base config not found: {base_config}")

    job_dir = scenario_dir / "output" / job_id
    if not job_dir.is_dir():
        raise SystemExit(f"job output dir not found: {job_dir}")

    trace_map = job_dir / "hops_trace_map.json"
    if not trace_map.exists():
        raise SystemExit(f"trace map not found: {trace_map}")

    derived_dir = job_dir / "derived"
    derived_dir.mkdir(parents=True, exist_ok=True)

    # Variant A: no_lookahead
    has_no_lookahead = _write_no_lookahead_summary(job_dir, derived_dir)
    if not has_no_lookahead:
        print("[warn] no hops_summary.json in job dir; variant 'no_lookahead' skipped")

    # Resolve link bench dir
    resolved_link_bench = link_bench_dir or (job_dir / "calibration" / "link_bench")
    links_overlay = derived_dir / "links.yaml"
    has_links = False
    if resolved_link_bench.is_dir():
        has_links = _generate_links_overlay(resolved_link_bench, trace_map, links_overlay)
    if not has_links:
        print(f"[warn] no link_bench data at {resolved_link_bench}; skipping link_calibrated and trace_replay")

    # Variant B: link_calibrated
    if has_links:
        link_cal_yaml = derived_dir / "hops.link_calibrated.yaml"
        materialize(base_config, [links_overlay], link_cal_yaml)
        _run_hops(link_cal_yaml, derived_dir / "hops_link_calibrated_summary.json")

    # Variant C: trace_replay (requires both link data and trace data)
    has_trace = False
    stage_timings_overlay = derived_dir / "stage_timings.yaml"
    if has_links:
        has_trace = _generate_stage_timings_overlay(job_dir, stage_timings_overlay, min_iteration)
    if has_trace:
        trace_yaml = derived_dir / "hops.trace_replay.yaml"
        materialize(base_config, [links_overlay, stage_timings_overlay], trace_yaml)
        _run_hops(trace_yaml, derived_dir / "hops_trace_replay_summary.json")
    elif has_links:
        print("[warn] no usable megatron_trace; variant 'trace_replay' skipped")

    # Comparison
    links_source = str(resolved_link_bench) if has_links else "(not available)"
    comparison_path, report_path = compare_run.run(job_dir, links_source=links_source)
    print(f"wrote {comparison_path}")
    print(f"wrote {report_path}")
    print()
    print(report_path.read_text(encoding="utf-8"))
    return comparison_path


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--scenario", required=True, help="Scenario dir containing hops.base.yaml")
    parser.add_argument("--job-id", required=True, help="Slurm job id (subdir under scenario/output/)")
    parser.add_argument(
        "--link-bench-dir",
        default=None,
        help="Override link bench dir. Defaults to <scenario>/output/<job-id>/calibration/link_bench",
    )
    parser.add_argument(
        "--min-iteration",
        type=int,
        default=0,
        help="Drop events with iteration < this value when fitting stage timings",
    )
    return parser.parse_args()


def main() -> None:
    args = _parse_args()
    link_bench_dir = Path(args.link_bench_dir) if args.link_bench_dir else None
    run(
        scenario_dir=Path(args.scenario),
        job_id=args.job_id,
        link_bench_dir=link_bench_dir,
        min_iteration=args.min_iteration,
    )


if __name__ == "__main__":
    main()
