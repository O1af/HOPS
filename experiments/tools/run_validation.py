"""One-shot validation orchestrator for a completed Slurm job.

Given a scenario directory (containing hops.base.yaml) and a Slurm job id
(whose output lives at <scenario>/output/<job-id>/), this tool materializes
three HOPS variants, runs each, and emits comparison.json + report.md under
<job-dir>/derived/.

Variants:
  - no_lookahead:    base HOPS config only (copied from <job-dir>/hops_summary.json).
  - link_calibrated: base + link overrides + framework-level overlays
                     (iteration_barrier, optimizer kernel distribution) fit from
                     the trace. These parameterize host/framework effects that
                     transfer across model shapes on the same cluster, not the
                     per-stage compute of this specific run, so link_calibrated
                     remains a legitimate "predict before measure" variant.
  - trace_replay:    link_calibrated + per-stage forward/backward distribution
                     fit from <job-dir>/megatron_trace/. Diagnostic only; by
                     construction overfit to the target trace.

If link_bench results are not found, link_calibrated and trace_replay are
skipped and the manifest records that.

Usage:
    uv run python experiments/tools/run_validation.py \\
        --scenario experiments/experiment_1/1_all_nodes \\
        --job-id 12 \\
        [--link-bench-dir <path>]
"""

from __future__ import annotations

import argparse
import json
import shutil
from pathlib import Path

import compare_run
from _common import repo_root, write_generated_yaml
from derive_megatron_stats import write_stage_timings_overlay
from materialize_hops_variant import materialize
from parse_link_bench import (
    _iter_jsonl_rows,
    _stage_to_device,
    build_overrides_yaml,
    derive_pair_measurements,
)


def _run_hops(config_path: Path, summary_path: Path) -> None:
    import yaml

    from hops.config import parse_config
    from hops.runtime import build_runtime

    with open(config_path, encoding="utf-8") as f:
        raw_config = yaml.safe_load(f)

    config = parse_config(raw_config)
    runtime = build_runtime(config)

    for _ in range(runtime.num_batches):
        runtime.pipeline.start_batch(runtime.num_microbatches)
        runtime.engine.run(stop_condition=lambda: runtime.pipeline.batch_complete)

    summary_path.parent.mkdir(parents=True, exist_ok=True)
    runtime.reporter.write_summary_json(str(summary_path))


def _write_no_lookahead_summary(job_dir: Path, derived_dir: Path, base_config: Path) -> bool:
    existing = job_dir / "hops_summary.json"
    destination = derived_dir / "hops_no_lookahead_summary.json"
    if existing.exists():
        shutil.copyfile(existing, destination)
        return True
    if not base_config.exists():
        return False
    _run_hops(base_config, destination)
    return True


def _generate_links_overlay(
    link_bench_dir: Path,
    trace_map_path: Path,
    output_path: Path,
) -> bool:
    jsonl_paths = sorted(link_bench_dir.glob("*.jsonl"))
    if not jsonl_paths:
        return False
    trace_map = json.loads(trace_map_path.read_text(encoding="utf-8"))
    stage_to_device = _stage_to_device(trace_map)
    rows = _iter_jsonl_rows(jsonl_paths)
    measurements = derive_pair_measurements(rows)
    document = build_overrides_yaml(measurements, stage_to_device)
    banner = (
        "# GENERATED by experiments/tools/run_validation.py (parse_link_bench). DO NOT EDIT.\n"
        f"# Source: {link_bench_dir}\n"
    )
    write_generated_yaml(output_path, banner, document)
    return True


def run(
    scenario_dir: Path,
    job_id: str,
    link_bench_dir: Path | None = None,
    min_iteration: int = 0,
) -> Path:
    base_config = scenario_dir / "hops.base.yaml"
    if not base_config.exists():
        raise SystemExit(f"base config not found: {base_config}")

    job_dir = scenario_dir / "output" / job_id
    if not job_dir.is_dir():
        raise SystemExit(f"job output dir not found: {job_dir}")

    trace_map = job_dir / "hops_trace_map.json"
    if not trace_map.exists():
        raise SystemExit(f"trace map not found: {trace_map}")

    derived_dir = job_dir / "derived"
    derived_dir.mkdir(parents=True, exist_ok=True)

    if not _write_no_lookahead_summary(job_dir, derived_dir, base_config):
        print("[warn] no hops_summary.json in job dir; variant 'no_lookahead' skipped")

    resolved_link_bench = link_bench_dir or (job_dir / "calibration" / "link_bench")
    links_overlay = derived_dir / "links.yaml"
    has_links = (
        resolved_link_bench.is_dir()
        and _generate_links_overlay(resolved_link_bench, trace_map, links_overlay)
    )
    if not has_links:
        print(f"[warn] no link_bench data at {resolved_link_bench}; skipping link_calibrated and trace_replay")

    has_trace = False
    has_optimizer = False
    has_barrier = False
    stage_timings_overlay = derived_dir / "stage_timings.yaml"
    optimizer_overlay = derived_dir / "optimizer.yaml"
    iteration_barrier_overlay = derived_dir / "iteration_barrier.yaml"
    if has_links:
        trace_dir = job_dir / "megatron_trace"
        if trace_dir.is_dir() and any(trace_dir.glob("*.jsonl")):
            forward_fits, _, optimizer_fit, barrier_fit = write_stage_timings_overlay(
                trace_dir,
                stage_timings_overlay,
                min_iteration,
                optimizer_overlay,
                iteration_barrier_overlay,
            )
            has_trace = bool(forward_fits)
            has_optimizer = optimizer_fit is not None
            has_barrier = barrier_fit is not None

    if has_links:
        link_cal_overlays = [links_overlay]
        if has_barrier:
            link_cal_overlays.append(iteration_barrier_overlay)
        if has_optimizer:
            link_cal_overlays.append(optimizer_overlay)
        link_cal_yaml = derived_dir / "hops.link_calibrated.yaml"
        materialize(base_config, link_cal_overlays, link_cal_yaml)
        _run_hops(link_cal_yaml, derived_dir / "hops_link_calibrated_summary.json")

    if has_trace:
        overlays = [links_overlay, stage_timings_overlay]
        if has_barrier:
            overlays.append(iteration_barrier_overlay)
        if has_optimizer:
            overlays.append(optimizer_overlay)
        trace_yaml = derived_dir / "hops.trace_replay.yaml"
        materialize(base_config, overlays, trace_yaml)
        _run_hops(trace_yaml, derived_dir / "hops_trace_replay_summary.json")
    elif has_links:
        print("[warn] no usable megatron_trace; variant 'trace_replay' skipped")

    links_source = str(resolved_link_bench) if has_links else "(not available)"
    comparison_path, report_path = compare_run.run(job_dir, links_source=links_source)
    print(f"wrote {comparison_path}")
    print(f"wrote {report_path}")
    print()
    print(report_path.read_text(encoding="utf-8"))
    return comparison_path


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--scenario", required=True, help="Scenario dir containing hops.base.yaml")
    parser.add_argument("--job-id", required=True, help="Slurm job id (subdir under scenario/output/)")
    parser.add_argument(
        "--link-bench-dir",
        default=None,
        help="Override link bench dir. Defaults to <scenario>/output/<job-id>/calibration/link_bench",
    )
    parser.add_argument(
        "--min-iteration",
        type=int,
        default=0,
        help="Drop events with iteration < this value when fitting stage timings",
    )
    return parser.parse_args()


def main() -> None:
    args = _parse_args()
    link_bench_dir = Path(args.link_bench_dir) if args.link_bench_dir else None
    run(
        scenario_dir=Path(args.scenario),
        job_id=args.job_id,
        link_bench_dir=link_bench_dir,
        min_iteration=args.min_iteration,
    )


if __name__ == "__main__":
    main()
