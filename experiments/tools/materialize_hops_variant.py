"""Merge a base HOPS config with generated overlay fragments.

Usage:
    uv run python experiments/tools/materialize_hops_variant.py \\
        --base scenario/hops.base.yaml \\
        --overlay job/derived/links.yaml \\
        --overlay job/derived/stage_timings.yaml \\
        --output job/derived/hops.trace_replay.yaml

Merge semantics:
- `overrides.links` in an overlay fully replaces base.overrides.links.
- `overrides.devices` in an overlay fully replaces base.overrides.devices.
- `pipeline.stages` in an overlay may contain sparse entries keyed by stage id;
  each entry's fields (e.g. `compute`) replace the corresponding fields on the
  matching base stage. Stage ordering in the base is preserved.

The output is validated via hops.config.parse_config before write. A banner
comment marks the file as machine-generated.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import yaml

from _common import ensure_hops_importable, write_generated_yaml


def _load_yaml(path: Path) -> dict:
    with path.open(encoding="utf-8") as handle:
        data = yaml.safe_load(handle)
    if data is None:
        return {}
    if not isinstance(data, dict):
        raise ValueError(f"expected mapping at top level of {path}, got {type(data).__name__}")
    return data


def _merge_stage_overlay(base_stages: list[dict], overlay_stages: list[dict]) -> list[dict]:
    by_id: dict[int, dict] = {}
    positional: list[dict] = []
    for item in overlay_stages:
        if "id" in item:
            by_id[int(item["id"])] = item
        else:
            positional.append(item)
    if positional and by_id:
        raise ValueError(
            "pipeline.stages overlay must be either entirely keyed by 'id' or "
            "entirely positional, not a mix"
        )
    if positional:
        if len(positional) != len(base_stages):
            raise ValueError(
                f"positional pipeline.stages overlay has {len(positional)} entries "
                f"but base has {len(base_stages)}"
            )
        iterator: list[tuple[int, dict]] = list(enumerate(positional))
    else:
        iterator = sorted(by_id.items())

    merged = [dict(stage) for stage in base_stages]
    for idx, overlay in iterator:
        if idx < 0 or idx >= len(merged):
            raise ValueError(f"stage overlay index {idx} out of range for {len(merged)} base stages")
        target = merged[idx]
        for key, value in overlay.items():
            if key == "id":
                continue
            target[key] = value
    return merged


_DEEP_MERGE_KEYS = {"optimizer"}


def merge_overlay(base: dict, overlay: dict) -> dict:
    merged: dict[str, object] = dict(base)

    overrides_base = dict(merged.get("overrides") or {})
    overrides_overlay = overlay.get("overrides") or {}
    for key in ("links", "devices"):
        if key in overrides_overlay:
            overrides_base[key] = overrides_overlay[key]
    if overrides_base:
        merged["overrides"] = overrides_base

    pipeline_overlay = overlay.get("pipeline") or {}
    if pipeline_overlay:
        pipeline_base = dict(merged.get("pipeline") or {})
        overlay_stages = pipeline_overlay.get("stages")
        if overlay_stages is not None:
            base_stages = list(pipeline_base.get("stages") or [])
            pipeline_base["stages"] = _merge_stage_overlay(base_stages, overlay_stages)
        for key, value in pipeline_overlay.items():
            if key == "stages":
                continue
            pipeline_base[key] = value
        merged["pipeline"] = pipeline_base

    for key, value in overlay.items():
        if key in ("overrides", "pipeline"):
            continue
        if key in _DEEP_MERGE_KEYS and isinstance(value, dict) and isinstance(merged.get(key), dict):
            combined = dict(merged[key])  # type: ignore[arg-type]
            combined.update(value)
            merged[key] = combined
        else:
            merged[key] = value

    return merged


def materialize(base_path: Path, overlay_paths: list[Path], output_path: Path) -> dict:
    merged = _load_yaml(base_path)
    for overlay_path in overlay_paths:
        merged = merge_overlay(merged, _load_yaml(overlay_path))

    ensure_hops_importable()
    from hops.config import parse_config  # type: ignore[import-not-found]

    parse_config(merged)

    banner = (
        "# GENERATED by experiments/tools/materialize_hops_variant.py. DO NOT EDIT.\n"
        f"# base: {base_path}\n"
    )
    for overlay_path in overlay_paths:
        banner += f"# overlay: {overlay_path}\n"
    write_generated_yaml(output_path, banner, merged)
    return merged


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--base", required=True, help="Path to hops.base.yaml")
    parser.add_argument(
        "--overlay",
        action="append",
        default=[],
        help="Overlay YAML fragment (repeatable, applied in order)",
    )
    parser.add_argument("--output", required=True, help="Path to write the merged YAML")
    return parser.parse_args()


def main() -> None:
    args = _parse_args()
    overlays = [Path(p) for p in args.overlay]
    materialize(Path(args.base), overlays, Path(args.output))
    print(f"wrote merged config with {len(overlays)} overlay(s) -> {args.output}")


if __name__ == "__main__":
    main()
